package fundamentosJava;

public class IfElse {
	public static void main (String args[]) {
		/*
		 * if (condição){
		 * executa caso a condição seja satisfeita
		 * }*/
		
		int idade = 16;
		if (idade >= 18 ) {
			System.out.println("Maior de idade");
		} else {
			System.out.println("Menor de idade");
		}
	}
}
