package fundamentosJava;

public class switchCase {
	public static void main (String args[]) {
		/*
		 * switch(1){
		 * case 1:
		 *     valor: 1
		 * case 2:
		 *     valor: 1
		 *     break;
		 *     
		 *     default:
		 *        valor padrão;
		 *     }
		 */
		
		
		
		
		switch(2) {
		case 1:
			System.out.println("Cartão de crédito Selecinado");
			break;
		case 2:
			System.out.println("Saldos da sua conta");
			break;
			
		default:
				System.out.println("Falar com atendete");
		}
	}
}
